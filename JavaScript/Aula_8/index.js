/* Operadores
    + -> Adição/Concatenação
    - -> Subtração
    / -> Divisão
    * -> Multiplicação
    ** -> Potenciação
    % -> Resto da divisão
    ++ -> Incremento
    -- -> Decremento
*/

const x = parseInt("5.2");
const y = parseFloat("5.2");
const z = Number("5.2");

console.log(x, y, z);
