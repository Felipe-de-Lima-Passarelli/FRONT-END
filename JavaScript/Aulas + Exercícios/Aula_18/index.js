/*
Operadores Lógicos
&& -> AND -> E -> Todas precisam ser verdadeiras
|| -> OR -> OU -> Apenas uma precisa ser verdadeira
! -> NOT -> NÃO
*/

console.log(true && true && true && true);
console.log(true && true && false && true);

console.log(true || false);
console.log(false || false);

console.log(!false);
console.log(!true);
