// Peça um número e exiba sua tabuada de 1 a 10.

let numero = 3;

for (let i = 1; i <= 10; i++) {
  console.log(`1 x ${i} = ${1 * i}`);
}

// Permita gerar a tabuada de vários números de uma vez (ex.: tabuada do 2, 5 e 7).

let numeros = [2, 5, 7];

for (number of numeros) {
  for (let i = 1; i <= 10; i++) {
    console.log(`${number} x ${i} = ${number * i}`);
  }
}
